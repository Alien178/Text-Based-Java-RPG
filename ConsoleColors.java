public class ConsoleColors {
    // Reset
    public static final String RESET = "\033[0m"; // Text Reset

    // Clear Console
    public static final String CLEAR = "\033[H\033[2J";

    // Regular Colors
    public static final String B = "\033[0;30m"; // BLACK
    public static final String R = "\033[0;31m"; // RED
    public static final String G = "\033[0;32m"; // GREEN
    public static final String Y = "\033[0;33m"; // YELLOW
    public static final String BL = "\033[0;34m"; // BLUE
    public static final String P = "\033[0;35m"; // PURPLE
    public static final String C = "\033[0;36m"; // CYAN
    public static final String W = "\033[0;37m"; // WHITE

    // Bold
    public static final String BB = "\033[1;30m"; // BOLD BLACK
    public static final String RB = "\033[1;31m"; // BOLD RED
    public static final String GB = "\033[1;32m"; // BOLD GREEN
    public static final String YB = "\033[1;33m"; // BOLD YELLOW
    public static final String BBL = "\033[1;34m"; // BOLD BLUE
    public static final String PB = "\033[1;35m"; // BOLD PURPLE
    public static final String CB = "\033[1;36m"; // BOLD CYAN
    public static final String WB = "\033[1;37m"; // BOLD WHITE
}